// Export helper function to get chunks by category
export function getChunksByCategory(category: string): ChunkData[] {
  return chunks.filter((chunk) => chunk.category === category)
}

// Export helper function to get chunks by subcategory
export function getChunksBySubcategory(subcategory: string): ChunkData[] {
  return chunks.filter((chunk) => chunk.subcategory === subcategory)
}

// Export helper function to get chunk by number
export function getChunkByNumber(number: number): ChunkData | undefined {
  return chunks.find((chunk) => chunk.number === number)
}

